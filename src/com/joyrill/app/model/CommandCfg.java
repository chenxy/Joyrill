package com.joyrill.app.model;

public class CommandCfg {
	private int commandTypeId;
	private int deviceTypeId;
	private String commandTypeName;
	private String commandTypeDisplayName;
	private String sommandTypeValue;
	private int enable;
	private int index;
	private String imgIco;
	private int displayType;
	public int getCommandTypeId() {
		return commandTypeId;
	}
	public void setCommandTypeId(int commandTypeId) {
		this.commandTypeId = commandTypeId;
	}
	public int getDeviceTypeId() {
		return deviceTypeId;
	}
	public void setDeviceTypeId(int deviceTypeId) {
		this.deviceTypeId = deviceTypeId;
	}
	public String getCommandTypeName() {
		return commandTypeName;
	}
	public void setCommandTypeName(String commandTypeName) {
		this.commandTypeName = commandTypeName;
	}
	public String getCommandTypeDisplayName() {
		return commandTypeDisplayName;
	}
	public void setCommandTypeDisplayName(String commandTypeDisplayName) {
		this.commandTypeDisplayName = commandTypeDisplayName;
	}
	public String getSommandTypeValue() {
		return sommandTypeValue;
	}
	public void setSommandTypeValue(String sommandTypeValue) {
		this.sommandTypeValue = sommandTypeValue;
	}
	public int getEnable() {
		return enable;
	}
	public void setEnable(int enable) {
		this.enable = enable;
	}
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public String getImgIco() {
		return imgIco;
	}
	public void setImgIco(String imgIco) {
		this.imgIco = imgIco;
	}
	public int getDisplayType() {
		return displayType;
	}
	public void setDisplayType(int displayType) {
		this.displayType = displayType;
	}
	
}
